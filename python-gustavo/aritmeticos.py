x = 10
y = 3

print(10 + 3)
print(x + 3)

print(x + y)  # Soma
print(x - y)  # Subtração
print(x * y)  # Multiplicação
print(x / y)  # Divisão

print(x ** y) # Exponenciação
print(x // y) # Divisão inteira
print(x % y)  # Módulo (resto da divisão) (quando quero saber se o núero é par ou ímpar. Se o resultado for 0, é par. Se for 1, é ímpar.)