# RELACIONAIS

a = 10
b = 11

# a = 10       # Atribuição de valor (estou dizendo que o valor de a é igual a 10)
print(a == b)  # Comparação de igualdade
print(a != b)  # Comparação de desigualdade
print(a > b)   # Maior que
print(a < b)   # Menor que
print(a >= b)  # Maior ou igual a
print(a <= b)  # Menor ou igual a
